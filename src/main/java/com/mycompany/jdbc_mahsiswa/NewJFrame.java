private void tampilData() {

    try {

        Connection conn =
                Koneksi.getConnection();

        String sql =
                "SELECT * FROM mahasiswa";

        PreparedStatement ps =
                conn.prepareStatement(sql);

        ResultSet rs =
                ps.executeQuery();

        txtID.setText("");
        txtNama.setText("");

        while(rs.next()) {

            txtID.append(
                    rs.getInt("id") + "\n"
            );

            txtNama.append(
                    rs.getString("nama") + "\n"
            );
        }

    } catch(Exception e) {

        System.out.println(e);

    }
}
