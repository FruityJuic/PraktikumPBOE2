        Connection conn =
                Koneksi.getConnection();

        String sql =
                "INSERT INTO mahasiswa(nama) VALUES(?)";

        PreparedStatement ps =
                conn.prepareStatement(sql);

        ps.setString(
                1,
        IsiNama.getText()
        );

        ps.executeUpdate();

        tampilData();

        IsiNama.setText("");

